#include "fractol.h"

void    mess(int fd, char *str)
{
    int i;

    if (fd < 0 || !str)
        return ;
    if (*str)
    {
        i = write(fd, str, 1);
        if (i == -1)
            exit(1);
        mess(fd, str + 1);
    }
}